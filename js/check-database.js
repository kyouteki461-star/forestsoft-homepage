// Script to check and create news table if it doesn't exist
async function checkDatabase() {
    const { supabase } = window.supabaseConfig;

    try {
        // Check if news table exists by trying to select from it
        const { data, error } = await supabase
            .from('news')
            .select('*')
            .limit(1);

        if (error && error.code === 'PGRST116') {
            // Table doesn't exist, create it
            console.log('News table does not exist, creating...');
            await createNewsTable();
        } else if (error) {
            console.error('Error checking table:', error);
        } else {
            console.log('News table exists with', data.length, 'records');
        }
    } catch (error) {
        console.error('Database check error:', error);
    }
}

async function createNewsTable() {
    const { supabase } = window.supabaseConfig;

    // Note: In a real application, you need to create the table using Supabase SQL editor
    // This script just shows what the table structure should look like

    console.log('Please create the table in Supabase SQL Editor with the following SQL:');
    console.log(`
    -- Create news table
    CREATE TABLE public.news (
        id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
        title TEXT NOT NULL,
        date TEXT NOT NULL,
        content TEXT NOT NULL,
        created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW(),
        updated_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
    );

    -- Enable RLS (Row Level Security)
    ALTER TABLE public.news ENABLE ROW LEVEL SECURITY;

    -- Create policy for public access
    CREATE POLICY "Public access to news" ON public.news
        FOR SELECT USING (true);

    -- Create function to update updated_at timestamp
    CREATE OR REPLACE FUNCTION public.handle_updated_at()
    RETURNS TRIGGER AS $$
    BEGIN
        NEW.updated_at = NOW();
        RETURN NEW;
    END;
    $$ LANGUAGE plpgsql;

    -- Create trigger to update updated_at
    CREATE TRIGGER handle_news_updated_at
        BEFORE UPDATE ON public.news
        FOR EACH ROW
        EXECUTE FUNCTION public.handle_updated_at();
    `);

    alert('请在 Supabase 仪表板中执行上面的 SQL 语句来创建 news 表');
}

// Run check when page loads
document.addEventListener('DOMContentLoaded', checkDatabase);